export const calculatorMessages: Record<CalcLocale, CalcMessages> = { ko, ja };

export function formatCalcNumber(n: number, locale: CalcLocale): string {
  return n.toLocaleString(locale === "ja" ? "ja-JP" : "ko-KR");
}

export function productLabel(locale: CalcLocale, pt: ProductType): string {
  return locale === "ja"
    ? pt === "sheet_shutter"
      ? ja.productSheet
      : ja.productGarage
    : pt === "sheet_shutter"
      ? ko.productSheet
      : ko.productGarage;
}

export function garagePanelLabel(locale: CalcLocale, gt: GaragePanelType): string {
  const j: Record<GaragePanelType, string> = {
    base: ja.garageBase,
    wood: ja.garageWood,
    dark: ja.garageDark,
    premium: ja.garagePremium,
  };
  const k: Record<GaragePanelType, string> = {
    base: ko.garageBase,
    wood: ko.garageWood,
    dark: ko.garageDark,
    premium: ko.garagePremium,
  };
  return locale === "ja" ? j[gt] : k[gt];
}
